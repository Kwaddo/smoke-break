function getSelectedMap() {
    const urlParams = new URLSearchParams(window.location.search);
    const mapNumber = urlParams.get('map') || '1';
    if (mapNumber < 1 || mapNumber > 3 || isNaN(mapNumber)) return `map${1}`
    return `map${mapNumber}`;
}

function createTilemap() {
    const selectedMap = getSelectedMap();
    const currentMapLayout = mapLayouts[selectedMap];
    
    tilemapContainer.innerHTML = '';
    
    tilemapContainer.style.width = `${columns * tileSize}px`;
    tilemapContainer.style.height = `${rows * tileSize}px`;

    mapElement.style.width = `${columns * tileSize}px`;
    mapElement.style.height = `${rows * tileSize}px`;
    mapElement.style.backgroundSize = `${columns * tileSize}px ${rows * tileSize}px`;
    mapElement.style.backgroundImage = `url('./assets/images/${selectedMap}.png')`;

    tilemapContainer.style.gridTemplateColumns = `repeat(${columns}, ${tileSize}px)`;
    tilemapContainer.style.gridTemplateRows = `repeat(${rows}, ${tileSize}px)`;

    tiles = [];
    for (let row = 0; row < rows; row++) {
        for (let col = 0; col < columns; col++) {
            const tile = document.createElement('div');
            tile.classList.add('tile');
            if (currentMapLayout[row] && currentMapLayout[row][col] === 1) {
                tile.classList.add('wall');
            } else {
                tile.classList.add('empty');
            }

            tilemapContainer.appendChild(tile);
            tiles.push(tile);
        }
    }
}

function isValidMove(x, y) {
    const selectedMap = getSelectedMap();
    const currentMapLayout = mapLayouts[selectedMap];
    
    if (x < 0 || x >= columns || y < 0 || y >= rows) {
        return false;
    }
    return currentMapLayout[y][x] === 0;
}

function switchMap(mapNumber) {
    currentMap = mapNumber;
    const newUrl = new URL(window.location);
    newUrl.searchParams.set('map', mapNumber);
    window.history.pushState({}, '', newUrl);
    const selectedMap = `map${mapNumber}`;
    mapElement.style.backgroundImage = `url('./assets/images/${selectedMap}.png')`;
    tilemapContainer.innerHTML = '';
    createTilemap();
    if (typeof resetGame === 'function') {
        resetGame(false);
    }
}

function switchMapMulti(mapNumber) {
    if (typeof endGame === 'function') {
        endGame();
    }
    currentMap = mapNumber;
    const newUrl = new URL(window.location);
    newUrl.searchParams.set('map', mapNumber);
    window.history.pushState({}, '', newUrl);
    const selectedMap = `map${mapNumber}`;
    mapElement.style.backgroundImage = `url('./assets/images/${selectedMap}.png')`;
    tilemapContainer.innerHTML = '';
    createTilemap();
    const gameMenu = document.getElementById("game-menu");
    if (gameMenu && !gameStarted) {
        gameMenu.style.display = "flex";
    }
    if (gameStarted) {
        if (typeof resetGame === 'function') {
            resetGame(true);
            initMultiplayerGame();
        }
    }
    window.dispatchEvent(new CustomEvent('mapchange', { 
        detail: { mapNumber: currentMap }
    }));
}

